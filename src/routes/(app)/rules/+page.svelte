        <h2 class="text-center mb-4">Rules of Explaining</h2>
        <h3>Be Nice:</h3>
        <ul>
          <li>Stay respectful, polite, and friendly at all times.</li>
          <li>Don't insult people or their good intentions, even if they seem rude or ill-informed.</li>
          <li>Acknowledge your sources to avoid plagiarism.</li>
          <li>Using bigoted slurs, directed at other users or not, is uncivil.</li>
        </ul>
        <h3>All Submissions Must Seek Objective Explanations:</h3>
        <ul>
          <li>ELI5 is meant to simplify complex concepts, not provide personal experiences, opinions, or speculation.</li>
          <li>Posts that ask for straightforward answers, facts, recent/current events, religion/politics, hypotheticals, whole topic overviews, or business/group/individual motivations will be immediately removed.</li>
        </ul>
        <h3>Explain for Laypeople:</h3>
        <ul>
          <li>Unless stated otherwise, assume no knowledge beyond a typical secondary education program.</li>
          <li>Avoid unexplained technical terms, but don't condescend. "Like I'm five" is a figure of speech meaning "keep it clear and simple."</li>
        </ul>
        <h3>ELI5 is for Factual Information, Not Opinions:</h3>
        <ul>
          <li>All comments must explain, ask about, correct, or address facts.</li>
          <li>Trying to change someone's opinion or influence them with yours is not appropriate.</li>
          <li>Do not implicitly or explicitly involve your personal view on the matter if you are unable to discuss or correct factual information.</li>
        </ul>
        <h3>Loaded Questions Are Not Allowed:</h3>
        <ul>
          <li>Loaded questions presume a controversial or not obviously true statement as fact.</li>
          <li>A non-loaded way to ask about the same topic should be used.</li>
        </ul>
        <h3>Don't Guess:</h3>
        <ul>
          <li>If you don't know how to explain something, you don't need to reply.</li>
          <li>If you have a partly educated guess, only comment on the parts you are sure of.</li>
        </ul>
        <h3>Don't Try to Trick the Bot:</h3>
        <ul>
          <li>If the automatic moderator removes your post, read the whole message it gives you and follow its advice.</li>
          <li>Evasion will result in a ban.</li>
        </ul>
        <h3>Be Mindful of the Subreddit's Purpose:</h3>
        <ul>
          <li>ELI5 is for explanations, not debates or arguments.</li>
          <li>Be open-minded and willing to learn from others.</li>
        </ul>
      </div>